


public class SquareCubeFourthPower{

    public static void main(String[] args) {
        int i = 3;
        System.out.println("Number\tSquare\tCube\tFourth Power");
         int square = i * i;
         int cube = i * i * i;
         int fourthPower = i * i * i * i;
         System.out.println(i + "\t" + square + "\t" + cube + "\t" + fourthPower);
        }
    }